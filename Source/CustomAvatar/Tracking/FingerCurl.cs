namespace CustomAvatar.Tracking
{
    public class FingerCurl
    {
        public float thumb;
        public float index;
        public float middle;
        public float ring;
        public float little;

        public FingerCurl(float thumb, float index, float middle, float ring, float little)
        {
            this.thumb = thumb;
            this.index = index;
            this.middle = middle;
            this.ring = ring;
            this.little = little;
        }
    }
}
